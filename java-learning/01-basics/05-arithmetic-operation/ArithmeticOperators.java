// This is a simple Java class to demonstrate basic arithmetic operators
public class ArithmeticOperators {
    // Main method: Entry point of the Java program
    public static void main(String[] args) {
        // Declare two integer variables a and b
        int a = 10;
        int b = 5;
        // Perform arithmetic operations:
        int sum = a + b;// Addition: sum of a and b
        int difference = a - b; // Subtraction: difference between a and b
        int product = a * b;// Multiplication: product of a and b
        int quotient = a / b;  // Division: quotient when a is divided by b
        int remainder = a % b;   // Modulo: remainder when a is divided by b
        System.out.println("Sum: " + sum);                 // 10 + 5 = 15
        System.out.println("Difference: " + difference);   // 10 - 5 = 5
        System.out.println("Product: " + product);         // 10 * 5 = 50
        System.out.println("Quotient: " + quotient);       // 10 / 5 = 2
        System.out.println("Remainder: " + remainder);     // 10 % 5 = 0

    } 
}
